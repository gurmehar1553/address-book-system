public class Contacts {
    String fname, lname, city, state, email;
    int zip, phone;
    Contacts(String first,String last,String city_add,String state_add,String mail,int zip_add, int phoneNo){
        fname = first;
        lname = last;
        city = city_add;
        state = state_add;
        email = mail;
        zip = zip_add;
        phone = phoneNo;
    }
}
